	public static boolean isConnectingToInternet(Context context) {
		ConnectivityManager connectivity = (ConnectivityManager) context
				.getSystemService(Context.CONNECTIVITY_SERVICE);
		if (connectivity != null) {
			NetworkInfo[] info = connectivity.getAllNetworkInfo();
			if (info != null)
				for (int i = 0; i < info.length; i++)
					if (info[i].getState() == NetworkInfo.State.CONNECTED) {
						return true;
					}

		}
		return false;
	}
	
	public static String getLanguageHeaderToSet(){
		String currentDefaultLanguage = Locale.getDefault().getLanguage();
		if (currentDefaultLanguage.equals("en")){
			return "en";
		}else if (currentDefaultLanguage.equals("es")){
			return "es";
		}else if (currentDefaultLanguage.equals("tr")){
			return "tr";
		}else if (currentDefaultLanguage.equals("ja")){
			return "ja";
		}else{
			return "en";
		}
				
	}

